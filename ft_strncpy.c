#include "libft.h"

char	*ft_strncpy(char *dest, const char *src, size_t len)
{
	size_t				index;
	unsigned char		n;

	index = 0;
	n = (unsigned char)len;
	while (src[index] != '\0' && index < n)
	{
		dest[index] = src[index];
		index++;
	}
	while (index < n)
	{
		dest[index] = '\0';
		index++;
	}
	return (dest);
}
